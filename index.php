            <h1>Diabetes </h1>
            <p class="formata_texto">
             <h3>O que é: </h3> <br><br><p class="formata_texto">A diabetes é uma doença crônica que ocorre quando o pâncreas não é capaz de produzir insulina, a sua produção é insuficiente ou quando o corpo não é capaz de fazer bom uso da insulina que produz.
              A insulina é um hormônio produzido pelo pâncreas para metabolizar a glicose e, assim, transformá-la em energia para o corpo. A incapacidade de produzir insulina e a impossibilidade de usá-la de forma eficaz resultam em níveis de glicose elevados no sangue: a hiperglicemia, que caracteriza o diabetes. Ao longo do tempo, altos níveis de glicose podem gerar danos para os rins, olhos, coração, nervos, vasos sanguíneos, entre outras complicações.
             
             
          
            
            <img src="assets/diabetes.jpg" class="rounded mx-auto d-block" alt="...">
          
            <h3>Tipos de Diabetes  </h3>
            <br>
            <h3>Tipo 1</h3>
          <p class="formata_texto">
            O diabetes tipo 1 acontece quando a produção de insulina do pâncreas é pouca ou nenhuma, pois as células responsáveis pela criação de insulina são destruídas pelo próprio sistema imunológico da pessoa. É influenciado por fatores hereditários e costumam ser detectados na infância ou na adolescência.

          </p>

          <hr>
          <br>
<br>
          <h3>Tipo 2
          </h3> <p class="formata_texto">
         
            No diabetes tipo 2, o pâncreas produz a insulina. Mas pode estar produzindo em quantidade inferior ao necessário ou o próprio corpo humano desenvolveu uma resistência ao hormônio. Assim, o metabolismo do açúcar no sangue torna-se insuficiente, causando o acúmulo de glicose.

          
          
              <br>
        <h3>Pré-diabetes </h3>
        Pré-diabetes é quando o nível de glicose no sangue está acima do normal, mas não o suficiente para ser considerado como diabetes. Costuma ser causado por sobrepeso, e pode estar associado com a pressão alta e as alterações no colesterol.

    
            <p></p></p>
            <hr>
            <br>
            <br>
          <h3>Diabetes gestacional </h3>
          <p class="formata_texto">
            O diabetes gestacional, que costumam ser identificados durante os exames do pré-natal, é semelhante ao tipo 2, mas o aumento do açúcar no sangue é um pouco menor. A condição aumenta o risco de complicações durante a gravidez e o parto. Por isso, deve ser monitorado com acompanhamento médico.



<hr>
<h3>Controle da diabetes</h3>
<p class="formata_texto">
  É importante controlar o nível de glicose no sangue, para evitar complicações. A medição pode ser feita por meio de um monitor de glicemia ou por meio de bombas de insulina.

  A glicemia normal em jejum não deverá ultrapassar os 100 mg/dL
  
  Duas horas após uma refeição, a glicemia não deverá ultrapassar 140 mg/dL
  
  
<hr>
<p></p>
                      <img src="imgs/causes-of-diabetes.png" class="rounded mx-auto d-block" alt="...">
       <h3>Tratamento da diabetes
      </h3><p><strong>ALIMENTAÇÃO ADEQUADA 
      </strong></p>

      <ul>
        <li>
          
          <p class="formata_texto">
              Manter uma alimentação saudável, regulando a quantidade de doces e gordura ingeridos, por exemplo.

              Para quem tem diabetes, uma ferramenta muito importante é a contagem de carboidratos. Você pode anotar os valores ou colocar também em aplicativos para o celular.
              
              <p><strong>EXERCÍCIOS FÍSICOS</strong></p>
              
              Exercícios físicos regulares ajudam a baixar as taxas de glicemia. Quando você gasta energia, o organismo usa o açúcar do sangue em velocidade maior.
              
              Caminhadas e percursos de bicicleta podem ser ótimas opções. Troque a escada rolante e o elevador pela escada comum. Pare o carro mais longe e ande alguns quarteirões. Meia hora de atividade moderada, três vezes por semana, já é uma boa meta.
              
              <p><strong>USO DE MEDICAÇÃO</strong></p>
              
              Os medicamentos para controle do diabetes estão sempre evoluindo, e o médico é a pessoa mais capacitada para indicar aquele que se adapta ao seu perfil. Eles ajudam o pâncreas a produzir mais insulina, diminuem a absorção de carboidratos e aumentam a sensibilidade do organismo à ação da insulina.
              
          <hr>
        </li>
      </ul>
