                <h1>Hipertensão Arterial Sistêmica</h1>
                <p class="formata_texto">
                 <h3>O que é: </h3>  <br><br><p class="formata_texto">Quando o coração bate, bombeia sangue para o corpo, fornecendo-lhe nutrientes e oxigênio. O sangue que circula faz pressão contra as paredes dos vasos sanguíneos. A força da pressão tem o nome de pressão arterial.
                 Quando a pressão arterial é demasiado elevada, exerce tensão nas paredes das artérias, tornando-as mais suscetíveis a depósitos de colesterol e ao desenvolvimento de placas ateroscleróticas (também chamadas artérias endurecidas).
                 A pressão alta faz o coração trabalhar mais. Isso faz com que o músculo cardíaco engrosse e endureça, o que pode levar a insuficiência cardíaca.
                </p>
                
                <img src="imgs/hipertencao.jpg" class="rounded mx-auto d-block" alt="...">
              
              <p class="formata_texto">
                A hipertensão arterial é definida como uma condição clínica quando o nível de pressão, particularmente da pressão arterial mínima, apresenta permanentemente elevação. “Toda a vez que medimos os níveis da pressão arterial, há a medida da pressão máxima e da pressão mínima O que significa isto: o primeiro valor (pressão máxima) está relacionado com a força de contração do coração (ventrículo esquerdo) para impulsionar o sangue, visando nutrir todos os órgãos e tecidos do corpo humano. O segundo valor (pressão mínima) diz respeito ao que se denomina de resistência periférica, ou seja, a força mecânica que o sangue deve vencer ao nível dos pequenos ramos arteriais dos órgãos e tecidos (arteríolas) para poder nutrir adequadamente os tecidos”, afirma o patologista Marcello Fabiano de Franco, da Sociedade Brasileira de Patologia (SBP).
              </p>
  
              <hr>
              <h3>Fatores de risco:</h3> <p class="formata_texto">
              O desenvolvimento da hipertensão não ocorre instantaneamente, há um conjunto de fatores que estão associados à sua evolução e agravo. Estes fatores são conhecidos como fatores de risco e, segundo as VI Diretrizes Brasileiras de Hipertensão Arterial, são:
              <p><strong><p class="formata_texto">idade, sexo/gênero e etnia, fatores socioeconômicos, ingestão de sal, excesso de peso e obesidade, ingestão de álcool, fator genético e sedentarismo. Além desses, outros autores acrescentam ainda o tabagismo e a não adesão ao tratamento,</strong></p>
            
              
            
                além disso, a apneia do sono pode contribuir para a hipertensão arterial existente ou agravá-la. O estresse tende a causar aumento da pressão arterial temporariamente, mas a pressão arterial geralmente retorna ao normal assim que o estresse termina. Com relação ao sexo/gênero, as VI Diretrizes Brasileiras de Hipertensão consideram que o masculino é o mais afetado pela doença até os 50 anos de idade e as mulheres, ao produzirem hormônios femininos que são fatores de proteção, tendem a ter a incidência baixa antes da menopausa a qual aumenta a partir da sexta década de vida
            
        
                <p></p></p>
                <hr>
  
              <h3>Incidência:</h3>
              <p class="formata_texto">
              A hipertensão acomete cerca de 30% da população adulta do Brasil, chegando a mais de 50% dos brasileiros com mais de 60 anos, de acordo com o Ministério da Saúde. A elevação da pressão ocorre principalmente porque os vasos 
              por onde circula o sangue se estreitam. Se não tratada, a hipertensão pode provocar complicações no coração, no cérebro e nos rins. Segundo a Sociedade Brasileira de Cardiologia, 
  <strong>a doença é responsável por 40% dos infartos, 80% dos derrames e 25% dos casos de insuficiência renal terminal.</strong>
                
  
  <hr>
    <h3>Sintomas:</h3>
    <p class="formata_texto">
    Na maioria das pessoas, a hipertensão arterial não causa sintomas, apesar da ocorrência concomitante de determinados sintomas que são ampla, porém erroneamente, atribuídos à hipertensão arterial: dores de cabeça, sangramento nasal, tontura, rubor facial e fadiga. Pessoas com hipertensão arterial podem apresentar esses sintomas, mas tais sintomas ocorrem com a mesma frequência em pessoas com pressão arterial normal.
    
    <hr>
    <p></p>
 
           <h3>Causas</h3><p><strong>A hipertensão arterial pode ser</strong></p>
  
          <ul>
            <li>
              <p><strong>Primária:</strong></p>A hipertensão arterial <strong>sem causa conhecida </strong>
              <p class="formata_texto">
              (anteriormente chamada essencial) é chamada hipertensão primária. Entre 85% e 95% das pessoas com hipertensão arterial têm hipertensão primária. Várias alterações no coração e nos vasos sanguíneos provavelmente se associam para aumentar a pressão arterial. Por exemplo, a quantidade de sangue bombeado por minuto (débito cardíaco) pode estar aumentada e a resistência ao fluxo de sangue também pode estar aumentada, pois os vasos sanguíneos sofrem constrição. O volume de sangue pode estar aumentado também. As razões para tais alterações não são totalmente conhecidas, mas parecem envolver uma anormalidade hereditária que afeta a constrição das arteríolas, o que ajuda a controlar a pressão arterial. Outras alterações podem contribuir para o aumento da pressão arterial, incluindo a acumulação de quantidades excessivas de sódio dentro das células e a redução da produção de substâncias que dilatam as arteríolas
              
              <hr>
            </li>
          </ul>
  
          <ul>
            <li>
              <p><strong>Secundária:</strong></p>A hipertensão arterial  <strong>com causa conhecida  </strong><p class="formata_texto">
              é chamada de hipertensão secundária. Entre 5% e 15% das pessoas com hipertensão arterial têm hipertensão secundária. <strong>Em muitas dessas pessoas, </strong>a hipertensão arterial deriva de uma doença renal, distúrbios hormonais, uso de certos medicamentos.
              <hr>
            </li>
          </ul>
  
  
         
          <h3>Complicações da HAS:</h3>
          Se não tratada, a hipertensão pode provocar complicações no coração, no cérebro e nos rins. Segundo a Sociedade Brasileira de Cardiologia
          
          <hr>
  <p></p>
  <h3>Tratamento: </h3>
  <p class="formata_texto">
  As formas de tratamento da hipertensão arterial envolvem, em primeiro lugar, determinar se a hipertensão é primária ou secundária. Quando é secundária, é importante tratar a doença de base que está causando o estado hipertensivo. Após a identificação do tipo hipertensivo, o médico poderá determinar o melhor tratamento para controle da hipertensão, fazendo com que ela mantenha estabilizada. É importante consultar um médico para o diagnóstico, a atenção primária, como as clínicas da família oferecem consultas, tratamento e acompanhamento gratuito. 
  
